# 8.   Обчислити висоту hb і медіану mс.

ab = ((8-0)**2+(7-0)**2)**0.5
bc = ((-8-8)**2+(9-7)**2)**0.5
ca = ((0-(-8))**2+(9-7)**2)**0.5

p = (ab+bc+ca)/2

hb = round(2/bc*(p*(p-ab)*(p-bc)*(p-ca))**0.5, 3)
print("hb =", hb)

mc = round(0.5*(2*ab**2+2*bc**2-ca**2)**0.5, 3)
print("mc =", mc)
